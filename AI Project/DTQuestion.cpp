#include "DTQuestion.h"

/****************************************************************************************************
****************************************************************************************************/
DTQuestion::DTQuestion(bool* value) {
    m_value = value;
}

/****************************************************************************************************
****************************************************************************************************/
void DTQuestion::makeDecision(Creature* creature) {
    if (*m_value == true) {
        m_trueBranch->makeDecision(creature);
    }
    else {
        m_falseBranch->makeDecision(creature);
    }
}

/****************************************************************************************************
****************************************************************************************************/
void DTQuestion::newNode(bool branch, DTNode* newNode) {
    if (branch == true) {
        m_trueBranch = newNode;
    }
    else {
        m_falseBranch = newNode;
    }
}