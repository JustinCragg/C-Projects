#include "Box.h"

/************************************************************************************************************
*************************************************************************************************************/
void Box::init(glm::vec2 pos, glm::vec2 extents, glm::vec4 colour) {
	m_pos = pos;
	m_extents = extents;
	m_colour = colour;
}

/************************************************************************************************************
*************************************************************************************************************/
void Box::update() {
	if (m_active == true) {
		aie::Gizmos::add2DAABBFilled(m_pos, m_extents, m_colour);
	}
}